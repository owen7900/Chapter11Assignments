    private void btnGoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoActionPerformed
        int numRings = Integer.parseInt(txtInput.getText());
        
        if(numRings < 3) return;
        
        else {
            txtArea.setText("");
            move(numRings, 1,3,2);
            txtOutput.setText("" + numMoves);
            numMoves = 0;
        }
    }//GEN-LAST:event_btnGoActionPerformed

    public void move(int n, int i, int j, int k){
        if (n > 0){
            numMoves ++;
            move(n-1, i,k,j);
            txtArea.append("\nMove ring " + n + "from Peg" + i + "to peg " + j);
            move(n-1, k,j,i);
        }
    }
    
